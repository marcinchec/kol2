package lancuch;

public class Numbers
{
    private int a;
    private int b;
    private String type;

    public Numbers(int a, int b, String type)
    {
        this.a = a;
        this.b = b;
        this.type = type;
    }

    public int getA()
    {
        return a;
    }

    public int getB()
    {
        return b;
    }

    public String getType()
    {
        return type;
    }
}
